#include <iostream>

using namespace std;

int main() {
    float n1, n2, suma = 0;

    cout << "Digita dos numeros para hallar su suma." << endl;
    cout << "Introduce el primer numero:" << endl;
    cin >> n1;
    cout << "Introduce el segundo numero:" << endl;
    cin >> n2;

    suma = n1 + n2;

    cout << "El resultado de la suma es = " << suma << endl;

    return 0;
}
